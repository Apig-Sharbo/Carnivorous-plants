<h1>Top 5 Most Bizzare Plants:</h1>

<!------------------------------------------1st thing--------------------------------------------->
<h2 id="1">1. Corpse Flower, Titan Arum</h2>
<div class="object ob1">
    <img src="images/Titan_arum.JPG" title="Amorphophallus titanum" alt="N/A lol" class="img1">
    <h3>Amorphophallus titanum</h3>
    <p class="descrp1">

        The Corpse Flower smells strongly like a rotting corpse, and it looks like it belongs in the movie Avatar. I
        would imagine that the smell helps prevent it from being eaten, but despite this the plant is still very rare;
        according to the IUCN it’s officially “threatened." It comes from the forests of Sumatra. It's not actually one
        big flower; it’s thousands upon thousands of little male and female flowers. These exude oils, while the center
        collects heat. The heat plus the oils create the smell that attracts the beetles that pollinate the flower. In
        case you were wondering, a mature plant can weigh 200 pounds.
    </p>
</div>
<!------------------------------------------2nd thing--------------------------------------------->
<h2 id="2">2. Elephant-Foot Yam</h2>
<div class="object ob2">
    <img src="images/Amorphophallus paeoniifolius.jpg" title="Amorphophallus paeoniifolius" alt="N/A lol" class="img2">
    <h3>Amorphophallus paeoniifolius</h3>
    <p class="descrp2">

        Guess who this cute little guy is related to? Yep, the Corpse Flower. Not only are they related, they also share
        many characteristics. For example this guy also smells like a corpse. These elephant-foot yams vary in color, as
        well. Some are pure white.

        The Elephant-Foot Yam might have the edge on the Corpse Flower in wackiness because it is edible. In one
        Southeast Asian culture they are grown as a delicacy, while in another they are a last-resort food source.
    </p>
</div>
<!------------------------------------------3rd thing--------------------------------------------->
<h2 id="3">3. Rafflesia: Another "Corpse Flower"</h2>
<div class="object ob3">
    <img src="images/Rafflesia arnoldii.jpg" title="Rafflesia arnoldii" alt="N/A lol" class="img3">
    <h3>Rafflesia arnoldii</h3>
    <p class="descrp3">

        Rafflesia is another "corpse flower" because of its stench (I promise this is the last plant here that smells
        like a corpse). It's unique in that it is the world’s largest single flower. It is also wacky because it has no
        stems, leaves, or roots, though it does seem to be a plant of some kind. Some think it’s related to fungi. The
        Kew Botanical Gardens website puts it in Class Equisetopsia, related to horsetails, but Wikipedia puts in
        Malphigiales (a large category including willows and flaxes).

        Want to find one so you can plant it in your backyard? Me too. However these are extremely hard to find. They
        live most of their lives as inconspicuous strands of tissue, parasitic on Tetrastigma vines in tropical
        rainforests, until the strands develop a little inconspicuous bud, which for a few brief days explosively
        transforms into the thing above.
    </p>
</div>
<!------------------------------------------4th thing--------------------------------------------->
<h2 id="4">4. Venus Flytrap</h2>
<div class="object ob4">
    <img src="images/venus-flytrap.jpg" title="Dionaea muscipula" alt="N/A lol" class="img4">
    <h3>Dionaea muscipula</h3>
    <p class="descrp4">

        It's carnivorous. Not many plants eat things other than sunlight (though you'll see a few in the list below).
        And even fewer (about four species) are capable of rapid movement. This makes the Venus Flytrap seem like, I
        don't know, it might be from another planet or something. Actually it’s from boggy areas of North and South
        Carolina, where the soil is poor in nutrients. It craves a high-nitrogen snack once in a while.

        You might wonder how the trap is triggered. Well, with trigger hairs. When two hairs are touched within 20
        seconds of each other, or if a single hair is touched twice, the trap snaps shut. Healthier Venus Flytraps close
        quicker. Scientists are unsure about the mechanism; it has to do with neighboring cells sending chemical
        messages to each other.

        The jaw-looking things act like interlocking fingers, or to the insect inside, prison bars. They do their best
        to restrain the insect from escaping. Then the rest is done with digestive juices. Read even more about Venus
        Flytraps from the Botanical Society of America.
    </p>
</div>
<!------------------------------------------5th thing--------------------------------------------->
<h2 id="5">5. Tropical Pitcher Plants</h2>
<div class="object ob5">
    <img src="images/Nepenthes species.jpg" title="Nepenthes species" alt="N/A lol" class="img5">
    <h3>Nepenthes species</h3>
    <p class="descrp5">

        What is it? Monkeys have been spotted drinking from it. Rats have been spotted partially digested in it, and
        some other stuff too (no, it doesn't smell like a corpse). About 150 species are known, mostly from the forests
        of Sumatra, Borneo, and the Philippines.

        The diet of a tropical pitcher plant includes just about anything that can fit into its pouch of sticky sap,
        including lizards, termites, spiders, and worms, though it seems to prefer smaller insects. The individual
        species have complex relationships with their ecosystems. One has evolved to host colonies of carpenter ants to
        clean up the leftovers from the larger carcasses, which if left lying around in the plant would lead to putrid
        conditions (we wouldn't want that). Some Nepenthes have evolved into toilet bowls for tree shrews, providing a
        shrew-sized perch and sweet exudate to attract shrews while they do their business. These plants get the bulk of
        their nitrogen from the tree-shrew dung they collect. Sound like I'm making this all up? I'm not.
    </p>
</div>
